#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*node;

	node = malloc(sizeof(t_list));
	if (!node)
		return (NULL);
	node->content = content;
	node->moves = 0;
	node->ref = 0;
	node->target = NULL;
	node->next = NULL;
	return (node);
}
